
public class PickUp {

	String date;
	int time;
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	public PickUp(String date, int time) {
		super();
		this.date = date;
		this.time = time;
	}
	
	
	
}
